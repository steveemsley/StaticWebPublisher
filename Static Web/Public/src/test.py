import unittest

from textnode import (
    TextNode,
    text_type_text,
    text_type_bold,
    text_type_italic,
    text_type_code,
    text_type_image,
    text_type_link
)
from htmlnode import HTMLNode, LeafNode, ParentNode
from inline_markdown import split_nodes_delimiter,split_nodes_image,split_nodes_link,text_to_textnodes

class TestTextNode(unittest.TestCase):
    def test_eq(self):
        node = TextNode("This is a text node", "bold", "Noof.com")
        node2 = TextNode("This is a text node", "bold", "Noof.com")
        self.assertEqual(node, node2)

class TestTextNode(unittest.TestCase):
    def test_eq(self):
        node2 = HTMLNode(tag = None, value = None, children = None, props = {"href": "https://www.google.com", "target": "_blank"})
        print(node2)

class TestLeafNode(unittest.TestCase):
    def test_eq(self):
        node1 = LeafNode(tag = "a", value = "This is a test of tags.", children = None, props = {"href": "https://www.google.com", "target": "_blank"})
        print(node1.to_html())
        node2 = LeafNode(tag = None, value = "This is a test of raw type", children = None, props = {"href": "https://www.google.com", "target": "_blank"})
        print(node2.to_html())

